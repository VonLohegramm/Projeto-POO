    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        matricula = daoMatricula.verificarMatricula(cmbTurma.getSelectedItem().toString(), txtCPFAluno.getText());
        if(rdnAvista.isSelected()){
            vista = new AVista(matricula.getaVista().getCode());
            vista.setAgencia(Integer.parseInt(txtAgencia.getText()));
            vista.setnCheque(Integer.parseInt(txtNcheque.getText()));
            vista.setPreData(txtDataPagto.getText());
            vista.setValor(Double.parseDouble(txtValor.getText()));
            daoAvista.alterar(vista);
        }else{
            prazo = new APrazo(matricula.getaPrazo().getCod());
            prazo.setDtVencimento(txtData1Vencto.getText());
            prazo.setQtdeMensalidade(Integer.parseInt(txtQtdMensal.getText()));
            prazo.setTaxaJuros(Double.parseDouble(txtJuros.getText()));
            prazo.setValor(Double.parseDouble(txtValor.getText()));
            daoPrazo.alterar(prazo);
        }
        matricula = new Matricula(txtDataMatricula.getText());
        turma = new Turma(cmbTurma.getSelectedItem().toString(), "");
        aluno = new Aluno(txtCPFAluno.getText(), "");

        daoMatricula.alterar(matricula);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        matricula = new Matricula(txtDataMatricula.getText());
        turma = new Turma(cmbTurma.getSelectedItem().toString(), "");
        aluno = new Aluno(txtCPFAluno.getText(), "");
        
        if(rdnAvista.isSelected()){
            vista = new AVista(0);
            vista.setAgencia(Integer.parseInt(txtAgencia.getText()));
            vista.setnCheque(Integer.parseInt(txtNcheque.getText()));
            vista.setPreData(txtDataPagto.getText());
            vista.setValor(Double.parseDouble(txtValor.getText()));
            int cod = daoAvista.inserir(vista);
            vista.setCode(cod);
            matricula.setaVista(vista);
        }else{
            prazo = new APrazo(0);
            prazo.setDtVencimento(txtData1Vencto.getText());
            prazo.setQtdeMensalidade(Integer.parseInt(txtQtdMensal.getText()));
            prazo.setTaxaJuros(Double.parseDouble(txtJuros.getText()));
            int cod = daoPrazo.inserir(prazo);
            prazo.setCod(cod);
            matricula.setaPrazo(prazo);
        }
        
        daoMatricula.inserir(matricula);
        
        
    }//GEN-LAST:event_btnInserirActionPerformed

