    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        matricula = daoMatricula.verificarMatricula(cmbTurma.getSelectedItem().toString(), txtCPFAluno.getText());
        if(matricula != null){
            btnConsultar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnConsultar.setEnabled(false);
            
            txtAgencia.setText(matricula.getData());
            if(matricula.getaPrazo() != null){
                txtQtdMensal.setText(Integer.toString(matricula.getaPrazo().getQtdeMensalidade()));
                txtJuros.setText(Double.toString(matricula.getaPrazo().getTaxaJuros()));
                txtData1Vencto.setText(matricula.getaPrazo().getDtVencimento());
            }else{
                txtAgencia.setText(Integer.toString(matricula.getaVista().getAgencia()));
                txtNcheque.setText(Integer.toString(matricula.getaVista().getnCheque()));
                txtDataPagto.setText(matricula.getaVista().getPreData());
            }
        }else{
            btnConsultar.setEnabled(false);
            btnInserir.setEnabled(true);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

